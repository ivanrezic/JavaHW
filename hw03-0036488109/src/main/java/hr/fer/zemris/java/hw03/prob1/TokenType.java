package hr.fer.zemris.java.hw03.prob1;

/**
 * The Enumeration which represents token types. There are four token types:
 * {@link #EOF}, {@link #WORD}, {@link #NUMBER}, {@link #SYMBOL};
 * 
 * @author Ivan
 */
public enum TokenType {

	/** Type which represents end of file. */
	EOF,
	/** Type which represents words. */
	WORD,
	/** Type which represents numbers. */
	NUMBER,
	/** Type which represents symbols. */
	SYMBOL
}
